char inkey(void)
{
    uint32_t flags = huart3.Instance->ISR;


    if((flags & UART_FLAG_RXNE) || (flags & UART_FLAG_ORE))
    {
        __HAL_UART_CLEAR_OREFLAG(&huart3);
        return (huart3.Instance->RDR);
    }
    else
        return 0;
}

